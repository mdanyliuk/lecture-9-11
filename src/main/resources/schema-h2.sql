CREATE TABLE club (
         id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL UNIQUE,
         name VARCHAR(255) NOT NULL);

CREATE TABLE player (
    id INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    position VARCHAR(255) NOT NULL,
    id_club INT NOT NULL);
ALTER TABLE player ADD CONSTRAINT fk_player_club FOREIGN KEY (id_club) REFERENCES club (id);
CREATE INDEX position_club ON player (position, id_club);

